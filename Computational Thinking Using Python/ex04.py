r = "s"

while(r == "s"):

    print ("")
    a = int(input("Primeiro valor: "))
    b = int(input("Segundo valor: "))
    c = int(input("Terceiro valor: "))
    d = int(input("Quarto valor: "))

    media = (a + b + c + d) / 4

    print("Média: ", media)

    print ("")
    r = input("Repetir(s/n)? ")

