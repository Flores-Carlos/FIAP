print("Exercício 5 - Conversão de Celsius para Fahrenheit")

r = "s"

while(r == "s"):

    print ("")
    c = float(input("Temperatura em Celsius: "))

    f = (c * 9 / 5) + 32

    print("Temperatura em Fahrenheit: ", f, "°F")

    print ("")
    r = input("Repetir(s/n)? ")

print ("")
print("Processo finalizado.")
